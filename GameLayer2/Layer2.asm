InitLayer2
;	di
	ld bc,LAYER2_ACCESS_PORT
	ld a,%00000011		; select middle section for writes
	out (c), a	; enable layer2

; clear the layer
	ld hl,0
	ld b,192
.loop1
	push bc
	ld a,b
	sub 1
	and a,%00111111
	ld h,a
	ld a,b
	sub 1
	and a,%11000000
	add %00000011

	ld bc,LAYER2_ACCESS_PORT
;	ld a,%00000011		; select middle section for writes
	out (c), a	; enable layer2


	ld b,0
.loop2
	ld l,b
	ld a,%11100011
	ld (hl),a
;;	inc hl
	djnz .loop2
	pop bc
	djnz .loop1


	ld b,0
.loopplot
	call Rand256
	ld l,a
	call Rand256
	and %00111111
	ld h,a
	call Rand256
;	and %00001111
	ld (hl),a
	djnz .loopplot

;	ld bc,LAYER2_ACCESS_PORT
